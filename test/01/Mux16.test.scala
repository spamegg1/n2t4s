import chisel3.*, simulator.ChiselSim
import munit.FunSuite

class Mux16Test extends FunSuite with ChiselSim:
  val Mux16Data = Map(
    ("b0000000000000000".U, "b0000000000000000".U, 0) -> "b0000000000000000".U,
    ("b0000000000000000".U, "b0000000000000000".U, 1) -> "b0000000000000000".U,
    ("b0000000000000000".U, "b0001001000110100".U, 0) -> "b0000000000000000".U,
    ("b0000000000000000".U, "b0001001000110100".U, 1) -> "b0001001000110100".U,
    ("b1001100001110110".U, "b0000000000000000".U, 0) -> "b1001100001110110".U,
    ("b1001100001110110".U, "b0000000000000000".U, 1) -> "b0000000000000000".U,
    ("b1010101010101010".U, "b0101010101010101".U, 0) -> "b1010101010101010".U,
    ("b1010101010101010".U, "b0101010101010101".U, 1) -> "b0101010101010101".U
  )

  test("Mux16"):
    simulate[Mux16](new Mux16()): mux16 =>
      for ((a, b, sel), out) <- Mux16Data do
        mux16.a.poke(a)
        mux16.b.poke(b)
        mux16.sel.poke(sel)
        mux16.clock.step(1)
        mux16.out.expect(out)
