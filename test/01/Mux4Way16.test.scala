import chisel3.*, simulator.ChiselSim
import munit.FunSuite

class Mux4Way16Test extends FunSuite with ChiselSim:
  val Mux4Way16Data = Map(
    (
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b00".U
    ) -> "b0000000000000000".U,
    (
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b01".U
    ) -> "b0000000000000000".U,
    (
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b10".U
    ) -> "b0000000000000000".U,
    (
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b0000000000000000".U,
      "b11".U
    ) -> "b0000000000000000".U,
    (
      "b0001001000110100".U,
      "b1001100001110110".U,
      "b1010101010101010".U,
      "b0101010101010101".U,
      "b00".U
    ) -> "b0001001000110100".U,
    (
      "b0001001000110100".U,
      "b1001100001110110".U,
      "b1010101010101010".U,
      "b0101010101010101".U,
      "b01".U
    ) -> "b1001100001110110".U,
    (
      "b0001001000110100".U,
      "b1001100001110110".U,
      "b1010101010101010".U,
      "b0101010101010101".U,
      "b10".U
    ) -> "b1010101010101010".U,
    (
      "b0001001000110100".U,
      "b1001100001110110".U,
      "b1010101010101010".U,
      "b0101010101010101".U,
      "b11".U
    ) -> "b0101010101010101".U
  )

  test("Mux4Way16"):
    simulate[Mux4Way16](new Mux4Way16()): mux4Way16 =>
      for ((a, b, c, d, sel), out) <- Mux4Way16Data do
        mux4Way16.a.poke(a)
        mux4Way16.b.poke(b)
        mux4Way16.c.poke(c)
        mux4Way16.d.poke(d)
        mux4Way16.sel.poke(sel)
        mux4Way16.clock.step(1)
        mux4Way16.out.expect(out)
